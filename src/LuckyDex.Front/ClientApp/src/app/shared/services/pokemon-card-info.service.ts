  public getPokemonCardInfo(id: string, selected: boolean = false): PokemonCardInfo {
    const info = this.infoDictionary[id];
    const imageUrl = `./assets/images/pokemon/${info.name.toLowerCase()}.png`;

    return <PokemonCardInfo> {
      id: id,
      name: info.name,
      obtainable: info.obtainable,
      regional: info.regional,
      generation: this.getGeneration(id),
      imageUrl: imageUrl,
      selected: selected,
    };
  }

  public getAllPokemonCardsInfo(ids: string[]): PokemonCardInfo[] {
    return Object.keys(this.infoDictionary)
      .map(id => this.getPokemonCardInfo(id, ids.includes(id)))
      .sort((c1, c2) => +c1.id - +c2.id);
  }

  private getGeneration(idString: string): string {
    const id = +idString;

    if (id >= 1 && id <= 151) { return 'Kanto'; }
    if (id >= 152 && id <= 251) { return 'Johto'; }
    if (id >= 252 && id <= 386) { return 'Hoenn'; }
    if (id >= 387 && id <= 493) { return 'Sinnoh'; }
    if (id >= 494 && id <= 649) { return 'Unova'; }
    if (id >= 650 && id <= 721) { return 'Kalos'; }
    if (id >= 722 && id <= 807) { return 'Alola'; }
    if (id >= 810 && id <= 890) { return 'Galar'; }
    return 'Unknown';
  }
